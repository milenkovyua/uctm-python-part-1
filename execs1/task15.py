'''
Да се състави програма, която отпечатва последователно на екрана
числата от 1 до 20. Да се използват две различни инструкции за цикъл.
'''

def main():
    # 1. Чрез for
    for i in range(1, 21):
        print(i)

    # 2. Чрез while
    i = 1
    while i <= 20:
        print(i)
        i += 1

main()