'''
Да се състави програма, която въвежда две числа от командния ред,
разделя първото на второто и извежда резултата от деленето.
Ако второто число е нула, да се изведе подходящо съобщение.
'''

def main():
    a = float(input("Enter a: "))
    b = float(input("Enter b: "))
    if b == 0:
        print("Division by zero.")
    else:
        print(a / b)

main()
